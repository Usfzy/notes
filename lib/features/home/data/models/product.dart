class Product {
  final String id;
  final String title;
  final String description;
  final double price;
  final int stockQty;
  final int productQty;

  const Product({
    required this.id,
    required this.title,
    required this.description,
    required this.price,
    required this.stockQty,
    required this.productQty,
  });
}
